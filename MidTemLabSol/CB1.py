print("1 KB = {} Bytes".format(2**10))
print("1 MB = {} Bytes".format(2**20))
print("1 GB = {} Bytes".format(2**30))
print("1 TB = {} Bytes".format(2**40))
print("1 PB = {} Bytes".format(2**50))
print("--------------------------------------------------")

print("1 KB = {} Bytes".format(1024))
print("1 MB = {} Bytes".format(1024*1024))
print("1 GB = {} Bytes".format(1024*1024*1024))
print("1 TB = {} Bytes".format(1024*1024*1024*1024))
print("1 PB = {} Bytes".format(1024*1024*1024*1024*1024))
