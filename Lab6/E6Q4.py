##Comparison Operator
##
##==, !=, >,<, >=, <=
##
##Write program(s) to demonstrate the use of all comparison operators.
##############################################################################

a=9
b=6
print(a==b) # It will print False
print(a!=b) # It will print True
print(a>b)  # It will print True
print(a<b)  # It will print False
print(a>=b) # It will print True
print(a<=b) # It will print False
